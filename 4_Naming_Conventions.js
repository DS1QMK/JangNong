/**
 * Naming Conventions
 * 
 * 1) 영어 (라틴문자)를 사용하며 문자와 숫자를 모두 사용할 수 있다.
 * 2) 특수기호는 언더스코어와 달러를 사용 할 수 있다.
 * 3) 숫자로 이름을 시작할 수 없다.
 * 4) 키워드는 변수명으로 사용 할 수 없다.
 *      var const = 'var'; (x)
 */

let Damwon = '담원'
var $damwon = '상민'
const _smPark = '박상민'

console.log(Damwon);
console.log($damwon);
console.log(_smPark);

